    private void txtCurrentRowKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCurrentRowKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String IndexRow = txtCurrentRow.getText();
            int rowIndex = 0;
            if(IndexRow.length()>1&&IndexRow.contains("/")){
               
                rowIndex = Integer.parseInt(IndexRow.substring(0, 1));
            }else{
                rowIndex = Integer.parseInt((txtCurrentRow.getText()));
            
            }
            try {
                crsBook.absolute(rowIndex);
                DisplayCurrentRow();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_txtCurrentRowKeyPressed

