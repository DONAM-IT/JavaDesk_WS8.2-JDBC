    public void GetBookList() throws Exception{
        Connection cnn = getConnection();
        try {
            String strSelectBooks = "select * from BookDetail";
            PreparedStatement ps =cnn.prepareStatement(strSelectBooks);
            ResultSet rs = ps.executeQuery();
            crsBook = new CachedRowSetImpl();
            crsBook.populate(rs);
            String strSelectPublisher = "Select PublisherID,PublisherName from Publisher";
            crsPublisher = new CachedRowSetImpl();
            crsPublisher.setCommand(strSelectPublisher);
            crsPublisher.execute(cnn);
            
            while(crsPublisher.next()){
                collectionPublisher.put(crsPublisher.getInt(1),crsPublisher.getString(2));
                cboPublisherName.addItem(crsPublisher.getString(2));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }finally{
            cnn.close();
        }
    }
    
    public void DisplayCurrentRow() throws Exception{
        txtBookID.setText(crsBook.getString("BookID"));
        txtBookTitle.setText(crsBook.getString("BookTitle"));
        txtBookPrice.setText((crsBook.getString("BookPrice")));
        txtBookQuantity.setText(crsBook.getString("BookQuantity"));
        
        txtCurrentRow.setText(crsBook.getRow()+"/"+rowCount);
        boolean disButtonNextLast = (crsBook.getRow()<rowCount);
        btnNext.setEnabled(disButtonNextLast);
        btnLast.setEnabled(disButtonNextLast);
        
        boolean disButtonPreviousFirst = (crsBook.getRow()>1);
        btnFirst.setEnabled(disButtonPreviousFirst);
        btnPrevious.setEnabled(disButtonPreviousFirst);
        
        int publisherID = crsBook.getInt("PublisherID");
        String publisherName = collectionPublisher.get(publisherID).toString();
        cboPublisherName.setSelectedItem(publisherName);
    }
    
